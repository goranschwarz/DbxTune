	/**
	 * Generate a summary report sorted by urgency (plain text)
	 */
	public String generateReport(List<PredictionResult> results)
	{
		results.sort((a, b) -> {
			if (!a._warningLevel.equals(b._warningLevel))
			{
				if (a._warningLevel.equals(CRITICAL)) return -1;
				if (b._warningLevel.equals(CRITICAL)) return 1;
				if (a._warningLevel.equals(WARNING))  return -1;
				if (b._warningLevel.equals(WARNING))  return 1;
			}
			return Long.compare(a._hoursUntilFull, b._hoursUntilFull);
		});
		
		StringBuilder report = new StringBuilder();
		report.append("================================================================================\n");
		report.append("DISK/DATABASE SPACE PREDICTION REPORT\n");
		report.append("Generated: ").append(formatTimestamp(LocalDateTime.now())).append("\n");
		report.append("================================================================================\n\n");
		
		long critical = results.stream().filter(r -> CRITICAL.equals(r._warningLevel)).count();
		long warning  = results.stream().filter(r -> WARNING .equals(r._warningLevel)).count();
		long ok       = results.stream().filter(r -> OK      .equals(r._warningLevel)).count();
		long insufficient = results.stream().filter(r -> !r._hasSufficientData).count();
		
		report.append("Summary:\n");
		report.append("  CRITICAL: ").append(critical    ).append("\n");
		report.append("  WARNING:  ").append(warning     ).append("\n");
		report.append("  OK:       ").append(ok          ).append("\n");
		report.append("  No Data:  ").append(insufficient).append("\n\n");
		
		for (PredictionResult result : results)
		{
			report.append("--------------------------------------------------------------------------------\n");
			report.append(result.toString());
			report.append("\n");
		}
		
		return report.toString();
	}
	
