	private String formatMb(double val)
	{
		double formatMbLessThanMbUsingOneDecimal = getFormatMbLessThanMbUsingOneDecimal();
		if (val < formatMbLessThanMbUsingOneDecimal)
		{
			return String.format("%.1f", val);
		}

		return String.format("%,.0f", val);		
	}
	
	private String escapeHtml(String text)
	{
		if (text == null) 
			return "";

		return text
				.replace("&", "&amp;")
				.replace("<",  "&lt;")
				.replace(">",  "&gt;")
				.replace("\"", "&quot;")
				.replace("'",  "&#39;");
	}
	
