	/**
	 * Predicts when some name/resource will become full
	 * 
	 * @param conn
	 * @param sql -- SQL Must return the following ResultSet
	 *               <ol>
	 *               	<li>String    -- Which is some kind of name</li>
	 *               	<li>String    -- Which is a extra/secondary name (could be: NULL or 'file system name' or 'DATA|WAL|LOG' for a DBMS)</li>
	 *               	<li>Timestamp -- When the sample was done</li>
//	 *               	<li>int/long  -- Total Size in KB</li>
//	 *               	<li>int/long  -- Used  Size in KB</li>
//	 *               	<li>int/long  -- Free  Size in KB</li>
	 *               	<li>double    -- Total Size in KB</li>
	 *               	<li>double    -- Used  Size in KB</li>
	 *               	<li>double    -- Free  Size in KB</li>
	 *               </ol>
	 *               AND also ORDER BY 
	 * @return
	 */
	public Set<PredictionResult> predictSpaceFull(String sql)
	throws SQLException
	{
		int percentileFilter = getPercentileFilter();

		// Keep data for each "name" (size group)
		LinkedHashMap<String, NamedDataSet> nameDataSet = new LinkedHashMap<>();

//System.out.println("SQL=" + sql);

		if (_logger.isDebugEnabled())
			_logger.debug("predictSpaceFull(): SQL: " + sql);

		try (Statement stmnt = _conn.createStatement())
		{
			stmnt.setQueryTimeout(0);
			
			try (ResultSet rs = stmnt.executeQuery(sql))
			{
				// Check ResultSet if it looks OK
				ResultSetMetaData rsmd = rs.getMetaData();

				int expectedColCount = 6;
				if (rsmd.getColumnCount() != expectedColCount)
				{
					throw new SQLException("Expected " + expectedColCount + " columns (String, String, Timestamp, Number, Number, Number). Received columns was " + rsmd.getColumnCount());
				}

				int nameDataType       = rsmd.getColumnType(1);
				int secondNameDataType = rsmd.getColumnType(2);
				int timestampDataType  = rsmd.getColumnType(3);
				int totalKBDataType    = rsmd.getColumnType(4);
				int usedKBDataType     = rsmd.getColumnType(5);
				int freeKBDataType     = rsmd.getColumnType(6);

				if ( ! ResultSetTableModel.isStringDataType   (nameDataType      )) throw new SQLException("Expected Column 1 (name) to be some kind of String."          + " Received columns JDBC Type was " + ResultSetTableModel.getColumnJavaSqlTypeName(nameDataType));
				if ( ! ResultSetTableModel.isStringDataType   (secondNameDataType)) throw new SQLException("Expected Column 2 (second name) to be some kind of String."  + " Received columns JDBC Type was " + ResultSetTableModel.getColumnJavaSqlTypeName(secondNameDataType));
				if ( ! ResultSetTableModel.isTimestampDataType(timestampDataType )) throw new SQLException("Expected Column 3 (timestamp) to be some kind of Timestamp." + " Received columns JDBC Type was " + ResultSetTableModel.getColumnJavaSqlTypeName(timestampDataType));
				if ( ! ResultSetTableModel.isNumberDataType   (totalKBDataType   )) throw new SQLException("Expected Column 4 (totalKB) to be some kind of Number."      + " Received columns JDBC Type was " + ResultSetTableModel.getColumnJavaSqlTypeName(totalKBDataType));
				if ( ! ResultSetTableModel.isNumberDataType   (usedKBDataType    )) throw new SQLException("Expected Column 5 (usedKB) to be some kind of Number."       + " Received columns JDBC Type was " + ResultSetTableModel.getColumnJavaSqlTypeName(usedKBDataType));
				if ( ! ResultSetTableModel.isNumberDataType   (freeKBDataType    )) throw new SQLException("Expected Column 6 (freeKB) to be some kind of Number."       + " Received columns JDBC Type was " + ResultSetTableModel.getColumnJavaSqlTypeName(freeKBDataType));
				
				String colLabelName       = rsmd.getColumnLabel(1);
				String colLabelSecondName = rsmd.getColumnLabel(2);

				while (rs.next())
				{
					String    name       = rs.getString   (1);
					String    secondName = rs.getString   (2);
					Timestamp ts         = rs.getTimestamp(3);
//					long      totalKB    = rs.getLong     (4);
//					long      usedKB     = rs.getLong     (5);
//					long      freeKB     = rs.getLong     (6);
					double    totalKB    = rs.getDouble   (4);
					double    usedKB     = rs.getDouble   (5);
					double    freeKB     = rs.getDouble   (6);

					// Get the correct "slot" -- if not found create one!
					NamedDataSet dataset = nameDataSet.computeIfAbsent(name, (key) -> new NamedDataSet(name, secondName, ts.toLocalDateTime()));
					
					if (_createSparkLines)
					{
						dataset._tsDataUsedKb.add(new TsDataPoint(ts, usedKB));
						dataset._tsDataFreeKb.add(new TsDataPoint(ts, freeKB));
					}
					
					LocalDateTime sampleTime = ts.toLocalDateTime();
					long hoursSinceStart = ChronoUnit.HOURS.between(dataset._startTime, sampleTime);

					dataset._rawData.add(new HourDataPoint(hoursSinceStart, usedKB));
					dataset._availableHistory.put(freeKB, sampleTime);

					if (freeKB < dataset._minAvailableKB) dataset._minAvailableKB = freeKB;
					if (freeKB > dataset._maxAvailableKB) dataset._maxAvailableKB = freeKB;

					dataset._currentUsedKB = usedKB;
					dataset._currentFreeKB = freeKB;
					dataset._sizeKB        = totalKB;

					dataset._colLabelName       = colLabelName;
					dataset._colLabelSecondName = colLabelSecondName;
				}
			}
		}

		/*
		 * Why linear regression is used instead of simple (last - first) / time:
		 *
		 * 1. DBMS growth is bursty:
		 *    - Index rebuilds
		 *    - Bulk loads
		 *    - Autogrowth events
		 *    - Log truncation
		 *
		 * 2. A single delta between two samples is extremely sensitive to:
		 *    - Sampling interval
		 *    - One-time maintenance operations
		 *    - Measurement jitter
		 *
		 * 3. Least-squares regression:
		 *    - Uses all historical points
		 *    - Averages out short-term spikes and drops
		 *    - Produces a statistically stable long-term growth rate
		 *
		 * The resulting slope (KB/hour) represents the best linear approximation
		 * of sustained storage consumption and is therefore more suitable for
		 * forward extrapolation than point-to-point deltas.
		 */

		/*
		 * DBMS caveats -- Especially for SQL Server (because space inside the files can grow/shrink AND the files can be do "autogrow":
		 *
		 * - Autogrowth events change total size in steps, not continuously.
		 * - Log files can shrink after backup/truncate, temporarily inverting slope.
		 * - Small databases may show quantization effects (MB-level reporting).
		 * - A near-zero slope can explode the "hours until full" estimate.
		 *
		 * Therefore:
		 *   - Ignore predictions when slope <= epsilon.
		 *   - Treat results as trend indicators, not guarantees.
		 */
		
		// This is what we will return;
		Set<PredictionResult> returnResult = new LinkedHashSet<>();

		// Loop each of the data sets and create the Prediction
		for (NamedDataSet namedEntry : nameDataSet.values())
		{
			PredictionResult result = new PredictionResult();

			returnResult.add(result);
//			namedEntry._result = result;
			
			result._resourceName       = namedEntry._name;
			result._secondaryName      = namedEntry._secondName;
			result._currentUsedKB      = namedEntry._currentUsedKB;
			result._currentFreeKB      = namedEntry._currentFreeKB;
			result._totalSizeKB        = namedEntry._sizeKB;
			result._usedPct            = namedEntry._sizeKB > 0 ? (namedEntry._currentUsedKB * 100.0 / namedEntry._sizeKB) : 0.0;
			result._dataPointsUsed     = namedEntry._rawData.size();
			result._hasSufficientData  = namedEntry._rawData.size() >= 10;
			
			result._minAvailableMB     = namedEntry._minAvailableKB / 1024;
			result._maxAvailableMB     = namedEntry._maxAvailableKB / 1024;
			
			result._tsDataUsedKb       = namedEntry._tsDataUsedKb;
			result._tsDataFreeKb       = namedEntry._tsDataFreeKb;

			result._colLabelName       = namedEntry._colLabelName;
			result._colLabelSecondName = namedEntry._colLabelSecondName;
			
			
//			List<Long> availableValues = new ArrayList<>(namedEntry._availableHistory.keySet());
			List<Double> availableValues = new ArrayList<>(namedEntry._availableHistory.keySet());
			Collections.sort(availableValues);
			if (!availableValues.isEmpty())
			{
				int lowPeakIndex = (int) (0.25 * availableValues.size());
				if (lowPeakIndex >= availableValues.size()) 
					lowPeakIndex = availableValues.size() - 1;
//				long lowPeakValue = availableValues.get(lowPeakIndex);
				double lowPeakValue = availableValues.get(lowPeakIndex);
				result._lowPeakAvailableMB = lowPeakValue / 1024;
				result._lowPeakTime        = namedEntry._availableHistory.get(lowPeakValue);
			}

			if (result._hasSufficientData)
			{
//				Map<Long, List<Long>> hourlyData = new HashMap<>();
				Map<Long, List<Double>> hourlyData = new HashMap<>();
				for (HourDataPoint dp : namedEntry._rawData)
				{
					hourlyData.computeIfAbsent(dp._hoursSinceStart, k -> new ArrayList<>()).add(dp._usedKB);
				}
				
				List<HourDataPoint> filteredData = new ArrayList<>();
//				for (Map.Entry<Long, List<Long>> hourEntry : hourlyData.entrySet())
				for (Map.Entry<Long, List<Double>> hourEntry : hourlyData.entrySet())
				{
//					List<Long> values = hourEntry.getValue();
					List<Double> values = hourEntry.getValue();
					Collections.sort(values);
					
					int percentileIndex = (int) ((percentileFilter / 100.0) * values.size());
					if (percentileIndex >= values.size()) 
						percentileIndex = values.size() - 1;

//					long percentileValue = values.get(percentileIndex);
					double percentileValue = values.get(percentileIndex);
					filteredData.add(new HourDataPoint(hourEntry.getKey(), percentileValue));
				}
				
				LinearRegressionResult regression = calculateLinearRegression(filteredData);
				
				result._growthRateKBPerHour = regression._slope;
				result._growthRateMBPerDay = (regression._slope * 24) / 1024.0;
				result._rSquared = regression._rSquared;
				
				if (regression._slope > 0)
				{
//					result._hoursUntilFull    = (long) (namedEntry._currentFreeKB / regression._slope);
					result._hoursUntilFull    = Math.round(regression._slope > EPSILON ? (namedEntry._currentFreeKB / regression._slope) : Double.POSITIVE_INFINITY);
//					result._hoursUntilFull    = Math.min(result._hoursUntilFull, Integer.MAX_VALUE);
					result._hoursUntilFull    = Math.min(result._hoursUntilFull, 9_999_999);
					result._predictedFullTime = LocalDateTime.now().plusHours(result._hoursUntilFull);
					
					if (result._hoursUntilFull < getCriticalThresholdHours())
					{
						result._warningLevel = CRITICAL;
					}
					else if (result._hoursUntilFull < getWarningThresholdHours())
					{
						result._warningLevel = WARNING;
					}
					else
					{
						result._warningLevel = OK;
					}
				}
				else
				{
					result._hoursUntilFull = -1;
					result._warningLevel = OK;
				}
			}
		}
		
		return returnResult;
	}

	/**
	 * Prediction result containing all relevant information
	 * <p>
	 * The names should be in plain naming (quoted identifiers will be added to the names)
	 * <p>
	 * <ul>
	 *   <li>The column names will be prefix/suffix with '[' and ']'</li>
	 *   <li>If you have any '[' character in any of the column name, it will NOT prefix/suffix the columns with '[' and ']' </li>
	 *   <li>The square brackets will be translated into DBMS specific Quoted Chars. <br>
	 *       so: <code>[name]</code> may be translated into <code>"name"</code></li>
	 *   <li>If you want to combine several 'names', you can do that using: <code>[name]||' -- '||[name2]</code><br>
	 *       Note: The '||' will be replaced with '+' on vendors that do not support ANSI SQL Concatenation '||'</li>
	 *   <li>If the columns names holds data in MB, then use 'srcDataSize=SourceDataSize.MB'...<br> 
	 *       Column names will then be modified to <code>[sizeMb] * 1024</code> </li>
	 * </ul>
	 * 
	 * @param srcDataSize      KB or MB
	 * @param nameCol          Column Name of the resource
	 * @param secondNameCol    Column Name of the extra/secondary name for a resource (could be: null, 'file system name', or 'DATA|WAL|LOG' for a DBMS)
	 * @param timestampCol     Column name holding the timestamp the data was sampled
	 * @param totalSizeCol     Column name holding "total" size of the resource
	 * @param usedSizeCol      Column name holding "used" size of the resource
	 * @param freeSizeCol      Column name holding "free" size of the resource
	 * @param schemaName       Schema name of the table (can be null)
	 * @param tableName        Table name to search
	 * @return
	 * @throws SQLException
	 */
	public Set<PredictionResult> predictSpaceFull(
		 SourceDataSize srcDataSize
		,String nameCol
		,String secondNameCol
		,String timestampCol
		,String totalSizeCol
		,String usedSizeCol
		,String freeSizeCol
		,String schemaName
		,String tableName
	)
	throws SQLException
	{
		int hoursOfHistory = getHoursOfHistory();

		String aliasNameCol       = "";
		String aliasSecondNameCol = "";
		
		// Construct COLUMN Names
		if ( ! nameCol      .contains("[") ) { nameCol       = "[" + nameCol       + "]"; aliasNameCol = " AS " + nameCol; }
		if ( ! timestampCol .contains("[") ) { timestampCol  = "[" + timestampCol  + "]"; }
		if ( ! totalSizeCol .contains("[") ) { totalSizeCol  = "[" + totalSizeCol  + "]"; }
		if ( ! usedSizeCol  .contains("[") ) { usedSizeCol   = "[" + usedSizeCol   + "]"; }
		if ( ! freeSizeCol  .contains("[") ) { freeSizeCol   = "[" + freeSizeCol   + "]"; }

		if (StringUtil.hasValue(secondNameCol))
		{
			if ( ! secondNameCol.contains("[") && ! secondNameCol.contains("'"))
			{
				secondNameCol = "[" + secondNameCol + "]"; 
				aliasSecondNameCol = " AS " + secondNameCol;
			}
			else
			{
				aliasSecondNameCol = " AS [Info]";
			}
		}
		else
		{
			secondNameCol = "''";
		}

		// Construct TABLE Name
		String table = "[" + tableName + "]";
		if (StringUtil.hasValue(schemaName))
			table = "[" + schemaName + "]." + table;

		// Construct WHERE Clause
		String sqlWhere = "";
		if (hoursOfHistory > 0)
		{
			LocalDateTime startTime = LocalDateTime.now().minusHours(hoursOfHistory);
			Timestamp startTimestamp = Timestamp.valueOf(startTime);
			
			sqlWhere = "  AND " + timestampCol + " >= '" + startTimestamp + "' \n";
		}
		
		// if columns are in MB make it into KB
		String multiplyer = "";
		if (SourceDataSize.MB.equals(srcDataSize))
			multiplyer = " * 1024";

		String sql = ""
			+ "SELECT \n"
			+ "     " + nameCol       + aliasNameCol + " \n"
			+ "    ," + secondNameCol + aliasSecondNameCol + " \n"
			+ "    ," + timestampCol  + " AS [Ts] \n"
//			+ "    ,CAST(" + totalSizeCol + " AS BIGINT) " + multiplyer + " \n"
//			+ "    ,CAST(" + usedSizeCol  + " AS BIGINT) " + multiplyer + " \n"
//			+ "    ,CAST(" + freeSizeCol  + " AS BIGINT) " + multiplyer + " \n"
			+ "    ,(" + totalSizeCol + " * 1.0) " + multiplyer + " AS [TotalSizeKb] \n"
			+ "    ,(" + usedSizeCol  + " * 1.0) " + multiplyer + " AS [UsedSizeKb] \n"
			+ "    ,(" + freeSizeCol  + " * 1.0) " + multiplyer + " AS [FreeSizeKb] \n"
			+ "FROM " + table + " \n"
			+ "WHERE 1 = 1 \n"
			+ sqlWhere
			+ "ORDER BY \n"
			+ "     " + nameCol      + " \n"
			+ "    ," + timestampCol + " \n"
			;

		// Special considerations for Vendors
		if (_conn.isDatabaseProduct(DbUtils.DB_PROD_NAME_MSSQL))
		{
			// Most SQL Server Versions does not have '||' for concatenation, so use '+' instead
			sql = sql.replace("||", "+");
		}
		
		
		sql = _conn.quotifySqlString(sql);

		return predictSpaceFull(sql);
	}

	
