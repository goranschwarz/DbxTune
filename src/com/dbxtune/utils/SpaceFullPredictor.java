///**
// * Predicts when a disk/file system will become full based on historical usage data.
// * Handles daily peaks by using percentile-based filtering.
// * 
// * Can be reused for:
// * - Filesystem space prediction (df command data)
// * - Database data/log space prediction (Sybase, SQL Server)
// * - Or any other when we have multiple samples of 'name', 'totalSize', 'usedSize', 'freeSize' stored in a DBMS Table
// */
/**
 * NOTE: Most of the Algorithms Used are generated by AI (I'm not a Math Wizard... So I took help from AI)
 * <p>
 * 
 * DiskFullPredictor estimates when a disk (or logical volume such as a DBMS
 * data or log file) will become full based on historical size and free-space samples.
 *
 * <p>Typical Input:</p>
 * <ul>
 *   <li>Time-series samples containing:
 *       <ul>
 *         <li>Resource Name -- Name of Disk or Database</li>
 *         <li>Timestamp</li>
 *         <li>Total size (KB)</li>
 *         <li>Used size (KB)</li>
 *         <li>Free size (KB)</li>
 *       </ul>
 *   </li>
 * </ul>
 *
 * <p>Computed metrics:</p>
 * <ul>
 *   <li>Used percentage = usedKB / totalKB * 100</li>
 *   <li>Growth rate (KB/hour) using linear regression</li>
 *   <li>Estimated hours until full = currentFreeKB / growthRate</li>
 * </ul>
 *
 * <p>Algorithm:</p>
 * <ol>
 *   <li>Samples are grouped by disk / file name.</li>
 *   <li>For each group, the code builds a time series of (time, usedKB) points.</li>
 *   <li>Simple least-squares linear regression is applied:
 *       <pre>
 *       y = a + b * x
 *       where:
 *         x = time (hours since first sample)
 *         y = used space in KB
 *         b = growth slope in KB/hour
 *       </pre>
 *   </li>
 *   <li>The slope {@code b} represents the average growth rate.</li>
 *   <li>Remaining time is extrapolated assuming linear growth:
 *       <pre>
 *       hoursUntilFull = currentFreeKB / b
 *       </pre>
 *   </li>
 * </ol>
 *
 * <p>Assumptions and limitations:</p>
 * <ul>
 *   <li>Growth is approximately linear over the observed period.</li>
 *   <li>No future autogrowth, file resizing, cleanup, or retention changes are modeled.</li>
 *   <li>Very small databases or nearly flat growth can produce unstable predictions
 *       due to floating-point noise or near-zero slopes.</li>
 *   <li>Predictions are invalid if the regression slope is zero or negative.</li>
 * </ul>
 *
 */
