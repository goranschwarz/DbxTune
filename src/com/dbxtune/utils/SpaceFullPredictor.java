		@Override
		public String toString()
		{
			StringBuilder sb = new StringBuilder();
			sb.append(getColnameResource() + ": ").append(getFmtResourceName()).append("\n");
			if (StringUtil.hasValue(getFmtSecondaryName()))
				sb.append(getColnameSecondary() + ": ").append(getFmtSecondaryName()).append("\n");
			sb.append("Current Usage: ").append(getFmtUsedPct())
				.append(" (").append(getFmtUsedMb()).append(" MB used, ")
				.append(getFmtFreeMb()).append(" MB available, ")
				.append(getFmtSizeMb()).append(" MB total)\n");
			
			if (!_hasSufficientData)
			{
				sb.append("Status: Insufficient data for prediction\n");
				return sb.toString();
			}
			
			sb.append("Available Space Stats: Min=").append(getFmtMinFreeMb())
				.append(" MB, Max=").append(getFmtMaxFreeMb())
				.append(" MB, Low-Peak=").append(getFmtLowPeakMb()).append(" MB");
			if (_lowPeakTime != null)
			{
				sb.append(" at ").append(getFmtLowPeakTime());
			}
			sb.append("\n");
			
			sb.append("Growth Rate: ").append(getFmtGrowthMbPerDay()).append(" MB/day")
				.append(" (").append(getFmtGrowthKbPerHour()).append(" KB/hour").append(")\n");
			sb.append("Prediction Quality (R²): ").append(getFmtRSquared()).append("\n");
			
			if (_growthRateKBPerHour <= 0)
			{
				sb.append("Status: Space usage is stable or decreasing\n");
			}
			else if (_hoursUntilFull < 0)
			{
				sb.append("Status: Unable to predict (negative timeline)\n");
			}
			else
			{
				sb.append("Predicted Full: ").append(getFmtPredictFullTime()).append("\n");
				sb.append("Time Until Full: ").append(getFmtHoursUntilFull()).append(" hours (")
					.append(getFmtDaysUntilFull()).append(" days)\n");
				sb.append("Warning Level: ").append(getFmtStatus()).append("\n");
			}
			
			return sb.toString();
		}

